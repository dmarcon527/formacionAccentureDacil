public class CalcularMultiplos {
	/**
	 * Calcula la suma de los 10 primeros números múltiplos de 5
	 * 
	 * @param args
	 */

	public static void main(String[] args) {

		for (int i = 1; i <= 10; i++) {
			System.out.println(5 * i);
		}

	}

}
