public class ListarNumeros {
	/**
	 * Este programa lo que hace es mostrar los números del 100 al 0 mostrandolos de
	 * 23 en 23
	 * 
	 * @param args
	 */

	public static void main(String[] args) {

		for (int i = 100; i > 0; i -= 23) {
			System.out.println(i);

		}

	}

}
